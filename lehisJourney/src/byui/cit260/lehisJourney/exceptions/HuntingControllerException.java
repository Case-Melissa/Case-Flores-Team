package byui.cit260.lehisJourney.exceptions;

/**
 *
 * @author Melissa Case
 */
public class HuntingControllerException extends Exception {

    public HuntingControllerException() {
    }

    public HuntingControllerException(String message) {
        super(message);
    }

    public HuntingControllerException(String message, Throwable cause) {
        super(message, cause);
    }

    public HuntingControllerException(Throwable cause) {
        super(cause);
    }

    public HuntingControllerException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
 

    
}
